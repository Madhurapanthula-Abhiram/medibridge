export const getIllnessById = (id) => illnesses.find(illness => illness.id === id);

export const getIllnessesByCategory = (category) =>
  illnesses.filter(illness => illness.category === category);

export const getCategories = () =>
  [...new Set(illnesses.map(illness => illness.category))];

export const searchIllnesses = (query) => {
  const lowerQuery = query.toLowerCase();
  return illnesses.filter(illness =>
    illness.name.toLowerCase().includes(lowerQuery) ||
    illness.symptoms.some(s => s.toLowerCase().includes(lowerQuery)) ||
    illness.category.toLowerCase().includes(lowerQuery)
  );
};
